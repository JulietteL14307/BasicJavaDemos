package com.java.basics;

// For loop is a conditional iterative statement which is used to check for certain conditions and then repeatedly execute a block of code as long as those conditions are met.

public class ForLoopDemo {
	
	public ForLoopDemo() {
		
	}
	
	public void forLoop() {
		
	System.out.println("* For Loop Demo *");
		
// This for loop begins at y=1 and will run until y=5
		
		for (int y = 1; y <= 5; y++)
			
			System.out.println("Value of y: " + y);
		
			System.out.println();
		
	}

}
